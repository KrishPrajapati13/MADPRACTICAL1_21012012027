fun main() {
    println("========================Type Conversion========================")

    val x = 5
    println("Integer : $x")
    x.toDouble()
    println("Integer to Double : $x ")

    val string = "10"
    println("String : $string")
    string.toInt()
    println("String To Integer : $string")

    val string2 = "10.5"
    println("String : $string2")
    string2.toDouble()
    println("String To Double : $string2 ")
}